package com.kunal;

public class Primitives {
    static void main(String[] args) {

        // primitives datatypes are datatypes which u cannot break further
        int number = 64;
        float smallDecimalValues = 89.76f;
        double longDecimalValues = 986453.865432322;
        long longIntegerValues = 987243462387879L;
        boolean check = true;
        char character = 'r';

        // String is non-primitive datatype because we can break further it into characters
        String name = "kunal";
    }
}
